#include <vector>

struct Str {
    const char *str;
    size_t size;
    template <size_t N>
    constexpr Str(const char (&str)[N]) : str{str}, size{N} {}
};

struct Escape {
    Str esc;
    const char *utf;
};

template <size_t N> struct SOAEscapes {
    Str esc[N];
    const char *utf[N];
    size_t size = N;
    template <typename... Seq>
    constexpr SOAEscapes(Seq &&... seq) : esc{seq.esc...}, utf{seq.utf...} {}
};

template <typename... Seq> constexpr auto mkesc(Seq &&... seq) {
    return SOAEscapes<sizeof...(Seq)>(seq...);
}

using It = std::vector<char>::iterator;

struct Range {
    It b, e;
    auto diff() const { return size_t(e - b); }
};

using Cause = bool (*)(const Range &);
using Effect = void (*)(const Range &);

struct Trans {
    Cause cause;
    Effect effect;
};

template <size_t N> struct Proc {
    Cause causes[N];
    Effect effects[N];
    template <typename... Seq>
    constexpr Proc(Seq &&... seq)
        : causes{seq.cause...}, effects{seq.effect...} {}
    auto apply(Range r) {
        for (size_t i = 0; i < N; ++i) {
            if (causes[i](r)) {
                return effects[i](r);
            }
        }
    }
};

template <typename... Seq> constexpr auto mkproc(Seq &&... seq) {
    return Proc<sizeof...(Seq)>(seq...);
}

