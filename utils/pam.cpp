void utf8(unsigned wc, unsigned char (&str)[5]) {
    if (wc <= 0x7F) {
        str[0] = uint8_t(wc & 0x7F);
        str[1] = '\0';
    } else if (wc <= 0x7FF) {
        str[1] = uint8_t(0x80 | (wc & 0x3F));
        wc = uint8_t(wc >> 6);
        str[0] = uint8_t(0xC0 | (wc & 0x1F));
        str[2] = '\0';
    } else if (wc <= 0xFFFF) {
        str[2] = uint8_t(0x80 | (wc & 0x3F));
        wc = uint8_t(wc >> 6);
        str[1] = uint8_t(0x80 | (wc & 0x3F));
        wc = uint8_t(wc >> 6);
        str[0] = uint8_t(0xE0 | (wc & 0xF));
        str[3] = '\0';
    } else if (wc <= 0x10FFFF) {
        str[3] = uint8_t(0x80 | (wc & 0x3F));
        wc = uint8_t(wc >> 6);
        str[2] = uint8_t(0x80 | (wc & 0x3F));
        wc = uint8_t(wc >> 6);
        str[1] = uint8_t(0x80 | (wc & 0x3F));
        wc = uint8_t(wc >> 6);
        str[0] = uint8_t(0xF0 | (wc & 0x7));
        str[4] = '\0';
    } else {
        str[2] = 0xEF;
        str[1] = 0xBF;
        str[0] = 0xBD;
        str[3] = '\0';
    }
}
auto amp(const Range &r) {
    unsigned char c[5] = {};
    if (r.diff() == 0) {
        return r.b;
    }
    auto it = r.b;
    if (*it++ != '&' || it == r.e) {
        goto FAIL;
    }
    if (*(it) == '#') {
        auto q = find(++it, r.e, ';');
        if (q == r.e) {
            goto FAIL;
        }
        auto size = distance(it, q);
        if (size < 1) {
            goto FAIL;
        }
        unsigned v = 0;
        unsigned base = 10;
        if (*it == 'x') {
            ++it;
            if (size <= 1) {
                goto FAIL;
            }
            base = 16;
        }
        for (unsigned i = 0; i < size; ++i) {
            if (*(it + i) < '0' || *(it + i) > '9') {
                goto FAIL;
            }
            v = v * base + *(it + i) % 48;
        }
        utf8(v, c);
        printf("%s", c);
        return it + size + 1;
    }
    for (size_t i = 0; i < escapes.size; ++i) {
        auto& e = escapes.esc[i];
        auto& u = escapes.utf[i];
        if (std::equal(e.str, e.str + e.size - 1, it)) {
            printf("%s", u);
            return it + e.size - 1;
        }
    }
FAIL:
    fwrite(&*r.b, sizeof(char), it - r.b, stdout);
    return it;
}

void putch(char c) {
    static auto last = '\0';
    if (last == ' ' && c == ' ') {
        return;
    } else if (c == '\n') {
        printf("\n%*s", nest, "");
    } else {
        putchar(c);
    }
    last = c;
}

Effect print = [](const Range &r) {
    if (!mute) {
        if (nl) {
            if (effects) {
                printf("\033[0;37m\n%*s", nest, "");
            } else {
                printf("\n%*s", nest, "");
            }
            nl = false;
            if (list) {
                printf("* ");
                list = false;
            }
        } else if(s) {
            if (effects) {
                printf("\033[0;37m ");
            } else {
                putchar(' ');
            }
            s = false;
        }
        auto ib = r.b, ie = r.e;
        for (; ib < r.e; ++ib) {
            if (!std::isspace(*ib)) {
                break;
            }
        }
        for (; ie > ib; --ie) {
            if (!std::isspace(*(ie - 1))) {
                break;
            }
        }
        if (ib == ie) {
            return;
        }
        if (effects) {
            printf("\033[%d;37m", style);
        }
        It n;
        while (ib != ie) {
            if (ib != (n = std::find(ib, ie, '&'))) {
                for (auto i = ib; i < n; ++i) {
                    putch(*i);
                }
            }
            ib = amp({n, ie});
        }
    }
};

int main(int args, char *[]) {
    if (args - 1 > 0) {
        effects = false;
    }
    std::vector<char> in(std::istreambuf_iterator<char>(std::cin), {});
    It ib(std::begin(in)), ie, end(std::end(in));
    while (ib != end) {
        if (*ib == '<') {
            if ((ie = std::find(ib, end, '>')) == end) {
                return 1;
            }
            proc.apply({ib + 1, ie});
            ib = ie + 1;
        } else {
            ie = std::find(ib, end, '<');
            print({ib, ie});
            ib = ie;
        }
    }
    if (effects) {
        printf("\033[0;37m\n");
    } else {
        putchar('\n');
    }
    return 0;
}


